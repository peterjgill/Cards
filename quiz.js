function check(){
	if(answers[random][random2].toLowerCase() == document.getElementById("answer").value.toLowerCase()){
		correct++;
		document.getElementById("correct").innerHTML = "correct: " + correct;
		ask();
	}else{
		incorrect++;
		document.getElementById("incorrect").innerHTML = "incorrect: " + incorrect;
	}
}

